# Prints 0-50
print(f"FIRST TASK")
for i in range(51):
    print(i)
# Prints 50 to 0
print(f"SECOND TASK")
for i in range(50, -1, -1):
    print(i)
# Prints 30 to 50
print(f'THIRD TASK')
for i in range(30, 51, +1):
    print(i)
# 50 to 10 by 2s
print(f'FOURTH TASK')
for i in range(50, 9, -2):
    print(i)
# 100 to 200 by 5s
print(f'FIFTH TASK')
for i in range(100, 201, +5):
    print(i)